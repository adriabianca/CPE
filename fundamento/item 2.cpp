// Fornecer dois números e mostre soma, o produto, a diferença e o quociente dos dois números

#include <iostream>
using namespace std;

int main() {
    float b1,b2;
    
    // dados
    
    cout <<"Primeiro numero:";
    cin >> b1;
    cout <<"Segundo numero:";
    cin >> b2;
    
    //operaçoes
    
    cout <<"Soma: "<< b1+b2<<endl;
    cout <<"Produto:"<< b1*b2<<endl;
    cout <<"Diferenca:"<< b1-b2<<endl;
    cout <<"Quociente:"<< b1/b2<< endl;
 

    return 0;

}
